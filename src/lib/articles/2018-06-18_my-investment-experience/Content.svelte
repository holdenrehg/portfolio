<p>
    <span class="first-letter">H</span>ere’s another portfolio from another millennial stock investor (me) who
    probably does not know what he is doing. Just so you have some back story on who I am and how I got to
    Robinhood, I am 24 years old and started investing when I was about 19 years old as a sophomore in
    college. I studied software engineering in school and knew very few friends or family who invested in the
    stock market. I barely knew people who had investment accounts like IRA’s. Some family members had 401k’s
    or pensions but they didn’t pay much attention to the inner workings of those accounts. They cared about a
    match from their employer into their “retirement account” and that they will be able to get that money at
    a certain age in life.
</p>
<p>
    So I went into this whole experience without any real exposure to investments, money management, or the
    financial industry as a whole. That’s the reason why I made the dumb mistake of setting up a Lending Club
    account. Here’s what I was thinking:
</p>

<h2>Why did I avoid the stock market?</h2>
<p />
<ul>
    <li>
        I wanted something in between a stock fund and individual stocks. I thought stocks were extremely
        risky and I thought stock funds were extremely conservative.
    </li>
    <li>
        I had no clue what a good return really was. I thought 15–20% would be something I could never get on
        the stock market without the chance of losing most of my money as well.
    </li>
    <li>
        Honestly, it just scared me a bit. There was such a high barrier to entry (this was before I started
        using Robinhood) so I didn’t want to mess something up.
    </li>
</ul>

<h2>Why did I think Lending Club was better?</h2>
<p>
    Lending Club seemed like the perfect setup for me. It is such an interesting idea in general and might
    work well for some people, but something I would never personally recommend after using it.
</p>
<ul>
    <li>
        <strong>Automation</strong>. It was similar to a robo adviser that handles everything for you. You
        just tell the system how much to invest. Of course it’s not buying or selling stocks, but it picks the
        micro loans that it wants to fulfill and handles incoming payments on those loans.
    </li>
    <li>
        It’s so <strong>easy to setup</strong>. In the same way that a Wealthfront trading account or Acorns
        account is easy to setup.
    </li>
    <li>
        It’s <strong>inherently low risk</strong>, with higher advertised returns than standard low risk
        investments. It has to be low risk because it uses extreme diversification. Your trading account will
        invest whatever you have in your account in $25 increments. I had about $7k in the account at the time
        which meant I had about 280 individual, $25 micro loans issued out to individuals. The chances of even
        10% of those defaulting on such small loans (considering you can set some requirements on income,
        employment status, etc.) is small. So the returns do come back pretty well.
    </li>
</ul>

<h2>Why was I wrong?</h2>
<p />
<ul>
    <li>
        <strong>It’s hard to get your money out.</strong> That might be an understatement. I cannot think of any
        reasonable investment where it is harder to get your money out. I started getting rid of my Lending Club
        account in 2015 and I’m still trying to get money out of it. It’s just the nature of loans. You have to
        either wait for the loan to run it’s course, which could be 15–30 years, or you go through the process
        of sending your loan notes on an open market. I opted to sell the notes and ultimately had to cut out all
        profit I made just to get the notes sold. I decided I would lose more money sitting on those notes than
        I would breaking even and getting those assets investing into something sound.
    </li>
    <li>
        It’s an extremely <strong>dangerous retirement investment</strong>. I am not concerned about using a
        trading account as a retirement account right now in my life, but there’s a lot of people out there
        who do expect to have that liquidity. I cannot imagine dumping a large amount of money into Lending
        Club at 45 years old, and realizing when I’m 65 that there’s a chance I have to wait at least another
        10 years to have full access to cash in the account (assuming it wasn’t auto investing past the
        initial investment.)
    </li>
    <li>
        <strong>You learn nothing</strong> about investing. At least with something like a Wealthfront trading
        account you get a small amount of exposure to what a stock portfolio looks like, what conservative returns
        look like, and what dividends are.
    </li>
</ul>

<h2>Then I picked Robinhood</h2>
<p>
    After that giant mess, I made about 12% returns on my Lending Club account, broke even overall because I
    had to sell all of my notes at a 12% loss to get most of my cash back, and setup a few different accounts.
    I setup a Wealthfront Roth IRA for retirement that I max out monthly, a Wealthfront automated trading
    account that I keep a very small amount of money in, and a Robinhood account.
</p>
<p>
    After a little over 8 months worth of research, and practical experience investing, here’s where I’ve
    ended up.
</p>
<p>
    There has been a lot of sells and buys to get to this specific place, so I’ve started keeping track of my
    own personal rules. I tried to create a set of guidelines so I’m not all over the place deciding what to
    do next.
</p>
<p>Here’s what my rules look like so far:</p>
<ol>
    <li>Ignore hype.</li>
    <li>Be patient and use cycles.</li>
    <li>Invest in what you know. Never invest strictly to “diversify your portfolio.”</li>
    <li>Learn at least 3 sectors.</li>
    <li>Invest 10 years ahead and recognize mismatches.</li>
    <li>Cut your losses.</li>
    <li>Read the financials.</li>
    <li>Check your portfolio once a week.</li>
</ol>

<h3>1. Ignore hype.</h3>
<p>
    This might be the hardest rule to follow especially for people who are just getting started. It’s hard not
    to google tips and find hundreds of articles saying things like “Next 3 Stocks Like Apple” or “The Next
    Amazon”. Remember that these articles are released every single day and the market changes every single
    day. You’ll go crazy trying to keep up with it all.
</p>
<p>Decide what type of investor you are, define your rules, and find your tools. Ignore everything else.</p>

<h3>2. Be patient and use cycles.</h3>
<p>
    The stock market obviously moves fast. There are people who make hundreds or even thousands of trades a
    day. There are also people who buy stock and hold on to it for decades.
</p>
<p>
    You gotta decide what type of investor you are, but for me personally I want to invest long term. The only
    way to do that, without becoming a miserable person, is to take your time and be patient. Once you know
    what you are looking for then you just have to wait.
</p>
<p>
    The other part of this is using cycles. For me, this was a way to force patience on my part. Within
    Robinhood you can setup deposits on an interval. I setup a routine deposit that happens once a week when
    my paycheck goes through, and then I let that money pile up in my trading account until I reach a certain
    limit. Then I take time to research and decide where to invest my available cash. This could start with
    something like a $50 weekly deposit, and a $200 cycle. That means you deposit into Robinhood once a week,
    and you buy stock once a month when you reach about $200 in your account. Eventually as you either start
    making more money, or start selling stocks more often, you’ll work up to higher deposits or higher cycles.
</p>

<h3>3. Invest in what you know. Never invest strictly to “diversify your portfolio.”</h3>
<p>
    This comes back to ignoring hype. There are too many way to make money on the market to count, but it is a
    bad idea to buy tech stocks if you know nothing about those companies or how they make their money. Just
    about every investor on the internet right now will tell you to buy Amazon stock, write long articles on
    why to buy Amazon stock, and you could even make high returns if you buy Amazon stock without much
    knowledge.
</p>
<p>
    When the market or a specific company is doing great, then everyone is going to win, but only knowledgable
    investors are going to know when to buy, sell, or hold at the right times. If you do not know anything
    about Amazon and strictly go off of financial reports for that company, or short term losses/gains then
    it’s going to make investing very difficult.
</p>
<p>
    Invest in what you know. I’m a software engineer so naturally I buy tech stocks. I have a strong grasp on
    every product offered by these tech companies, on the customers and needs for those products, and their
    long term visions. So when one of them misses their quarterly projections, jumps 5% in a single day, or
    gets some bad press, I have enough knowledge to know when to sell, buy, or hold.
</p>

<h3>4. Learn at least 3 sectors.</h3>
<p>
    People talk about diversification a lot when you start researching investments or investment strategies.
    Of course the more stocks you invest in or the more sectors you invest in, the “safer” you will be. The
    chance that 100 separate companies fail is much lower than 10 separate companies failing.
</p>
<p>
    But it also cuts into your returns. I personally invest in what I know as a way to create a safer
    investment. It’s more work this way to manage your portfolio, but done correctly you will do much better
    overall.
</p>
<p>So what if you only know 1 sector really well?</p>
<p>
    In that case, learn more. Spend the time, do the research, talk to friends and family, or find industry
    experts on LinkedIn. Learn at least 3 sectors, the companies within those sectors, and the products,
    customers, and vendors of those companies. This will make you a much better investor.
</p>

<h3>5. Invest 10 years ahead and recognize mismatches.</h3>
<p>
    Focus all of your attention on 10 years ahead. What companies that exist today will exist in 10 years? And
    which of those companies will be better off in 10 years? It’s not easy to decide which companies are going
    to do well, but learning the sectors and becoming an expert in those sectors lets you make an educated
    guess.
</p>
<p>Then buy the stock and expect to hold on.</p>
<p>There are two things to consider here:</p>
<ol>
    <li>
        Reevaluation. This is the process of reevaluating a company every so often to make sure they are still
        on track long term. You do not want to buy a stock, and hold it for 10 years without ever reevaluating
        it. I aim for a few times a month. That’s pretty manageable for my portfolio of around 20 stocks.
    </li>
    <li>
        What a lot of investors call “buying the dip.” <strong
            >This is not a huge deal for long term investors</strong
        >, because it would take a huge dip to matter much when looking over the course of 10 years.
        <strong>Do not try to time the market, </strong>but if you can, take advantage of what I call a
        mismatch (a drop in a stock price because of something that will not matter in 10 years if the company
        is solid, such as bad press or a bad financial quarter.) Follow your cycles, take the money that
        you’ve saved up, and be aware of available mismatches. If you are deciding between a few stocks,
        buying the dip on one could give you a few extra percent over the long term, but do not wait for a
        dip. Being strict and investing in solid companies will let everything work out over the long term.
    </li>
</ol>

<h3>6. Cut your losses.</h3>
<p>
    Know your investments. If you have bad opinions about a company, and their financials start to match your
    thoughts, then sell. Don’t let yourself lose too much just because you are emotionally tied to a
    stock/company. Stay analytical.
</p>
<p>
    Cutting your losses sooner than later let’s you use that cash in something better. I try to make sure to
    heavily reevaluate a company if it start slipping below -6% returns, but I will also allow a company to
    slip far below that if they are a long term, solid company that is going to be around in 10 years.
</p>

<h3>7. Read the financials.</h3>
<p>
    This is a simple one. Learn to read the companies financial. Being able to read a balance sheet is
    extremely helpful. I do not like to make sell/buy decision strictly based on financials but it helps me
    identify those mismatches I described, or just gives me a general idea of where the company is headed so
    that I can reevaluate if needed.
</p>

<h3>8. Check your portfolio once a week.</h3>
<p>
    Finally, just check up on your portfolio once a week. Reevaluate your companies, make sure companies are
    still headed in the right direction based on your vision of the company. This gives you time to make
    trades if absolutely needed.
</p>
<p>
    I say once a week, because I do not want to be constantly checking in with my investments to the point of
    obsession. That’s an easy thing to do especially with Robinhood because it’s so easily accessible.
</p>

